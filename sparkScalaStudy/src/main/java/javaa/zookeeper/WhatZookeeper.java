package javaa.zookeeper;

/**
 * zookeeper
 */
public class WhatZookeeper {
    /**
     * zookeeper提供两个功能
     * 1：保存数据
     * 2：提供监听
     *
     * 应用场景
     * 主从协调，服务器节点动态上下线，统一配置管理，分布式锁，统一名称服务
     * 1：提供统一的资源配置访问。（集群的配置，经常改动的）
     * 2：服务器的动态感知
     * 3：分布式共享锁
     * 4：选举
     *
     */

    /**
     * 每个节点在zookeeper中叫做znode
     * znode有两种类型
     * 一种是临时znode，断开连接自动删除
     * 一种是持久的znode，会保存下来，下次连接还有
     */
}
